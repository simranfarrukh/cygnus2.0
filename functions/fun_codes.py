from turtle import *


def heart():
    for i in range(200):
        right(1)
        forward(1)
        color('red', 'pink')
        begin_fill()
        left(140)
        forward(111.65)
        heart()
        left(120)
        heart()
        forward(111.65)
        end_fill()
        done()


